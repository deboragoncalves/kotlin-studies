/*
 * Exercício 6:
 * Desenvolva um algoritmo que calcule e 
 * imprima o total de dias em 7 meses, considerando que cada mês tenha 30 dias
 * 
 */

fun main(args: Array<String>) {
    val numberMonths : Int = 7
    val daysMonth : Int = 30

    var totalDays : Int = daysMonth * numberMonths
    
    println("Total de dias em 7 meses: $totalDays")
}
